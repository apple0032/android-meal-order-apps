package net.simplifiedcoding.bottomnavigationexample;

class WaitData {
    String Date;
    String Status;
    Integer Total;
    Integer Qty;
    Integer Id;
    public WaitData(String date, String status, Integer total, Integer qty, Integer id) {
        this.Date = date;
        this.Status = status;
        this.Total = total;
        this.Qty = qty;
        this.Id = id;
    }
}
